    private void btnRemoveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRemoveActionPerformed
        boolean isDeleted = false;
        customerController = new CustomerController();
        dtm = (DefaultTableModel) customerTable.getModel();
        int dialogoption = JOptionPane.showConfirmDialog(this, "Do you want to delete this customer?");

        if (dialogoption == JOptionPane.YES_OPTION) {
            int row = customerTable.getSelectedRow();
            int id = Integer.parseInt(dtm.getValueAt(row, 0).toString());
            isDeleted = customerController.deleteCustomer(id);
            dtm.setRowCount(0);
            loadCustomerTable();
        }
        loadCustomerTable();
    }//GEN-LAST:event_btnRemoveActionPerformed

