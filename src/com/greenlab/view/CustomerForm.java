    private void loadCustomerTable() {
        customerController = new CustomerController();
        dtm = (DefaultTableModel) customerTable.getModel();
        dtm.getDataVector().removeAllElements();
        List<Customer> customers = customerController.getAllCustomer();
        for (Customer c : customers) {
            Object[] rowData = {c.getId(), c.getName(), c.getAddress(), c.getTelphone1(), c.getTelphone2(), c.getTelphone3(), c.getEmail()};
            dtm.addRow(rowData);
        }
    }

    public void loadCustomers() {
        customerController = new CustomerController();
        List<Customer> customer = customerController.getAllCustomer();
        //chkProduct.setSelected(false);
        if (customer != null) {
            customerCombo.removeAllItems();
            customerCombo.addItem("None");
            for (Customer customer1 : customer) {
                customerCombo.addItem(customer1.getName());
            }
        }
    }
